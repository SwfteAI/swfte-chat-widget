<h1>Welcome to the docs</h1>

<p>
  This is a minimal SvelteKit app that ships two widgets at once: a floating chat bubble in the
  bottom-right, and a command-palette search modal triggered by <kbd>⌘K</kbd>. Both speak to the
  same Swfte agent and share state.
</p>

<h2>Try it</h2>

<ul>
  <li>Press <kbd>⌘K</kbd> (or <kbd>Ctrl</kbd>+<kbd>K</kbd>) to open the search modal.</li>
  <li>Click the bubble in the bottom-right to start a longer conversation.</li>
  <li>Send a message in the search modal, then close it — the conversation continues in the bubble.</li>
</ul>

